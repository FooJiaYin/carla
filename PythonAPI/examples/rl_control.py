    def step(self, action):
        if self.done:
            raise ValueError('self.done should always be False when calling step')
        
        for event in pygame.event.get():
            if event.type == pygame.QUIT:
                raise Exception("Quit")

        obs = None
        reward = 0
        success = False
        try:
            while True:
                # send control
                if self.target is None:
                    self.target =  self.map.get_waypoint(self.player.get_location()).next(15)[0].transform.location
                self.world.debug.draw_line(self.player.get_transform().location, self.target)
                control = self.action_converter.get_control(self.player.get_control(), action)
                self.player.apply_control(control)                

                # Gather observations                 
                obs = self.get_observation()
                
                # Get the reward
                # reward = 0
                reward, success = self.reward.get_reward(obs, self.target)
                break 
            
        except Exception as e:
            print(e)
    
        if len(self.collision_sensor.history) > 0:
            collision = True
        else:
            collision = False

        if self.steps >= 1000:
            timeout = True  
        else:
            timeout =  False

        if timeout:
            print('Timeout')
            self._failure_timeout = True
        if collision:
            print('Collision')
            self._failure_collision = True
        if success:
            print('Success')
        self.done = timeout or collision or success

        env_state = {'timeout': timeout, 'collision': collision, 'success': success}

        # Additional information
        info = {'timeout': timeout, 'collision': collision, 'success': success, 'done': self.done, 'carla-reward': reward}

        self.steps += 1
        return obs, reward, self.done, info


